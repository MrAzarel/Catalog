    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultComboBoxModel<TV> tvModel = (DefaultComboBoxModel<TV>) jList1.getModel();
        String brand = jTextField1.getText();
        String model = jTextField2.getText();
        String lifetimeS = jTextField3.getText();
        String priceS = jTextField4.getText();
        String lengthS = jTextField5.getText();
        String widthS = jTextField6.getText();
        String heightS = jTextField7.getText();
        int lifetime = 0;
        int price = 0;
        int length = 0;
        int width = 0;
        int height = 0;
        
        try{
            lifetime = Integer.parseInt(lifetimeS);
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "LifeTime must be integer number!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try{
            price = Integer.parseInt(priceS);
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Price must be integer number!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try{
            length = Integer.parseInt(lengthS);
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Length must be integer number!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try{
            width = Integer.parseInt(widthS);
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Width must be integer number!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try{
            height = Integer.parseInt(heightS);
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Height must be integer number!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        TV a = new TV(brand, model, lifetime, price, length, width, height);
        
        tvs.add(a);
        tvModel.addElement(new TV(brand, model, lifetime, price, length, width, height));
    }//GEN-LAST:event_jButton1ActionPerformed

