    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String brand = jTextField1.getText();
        String model = jTextField2.getText();
        String lifetimeS = jTextField3.getText();
        String priceS = jTextField4.getText();
        String lengthS = jTextField5.getText();
        String widthS = jTextField9.getText();
        String heigthS = jTextField10.getText();
        if(brand.equals("")){
            JOptionPane.showMessageDialog(this, "Brand must be not empty!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(model.equals("")){
            JOptionPane.showMessageDialog(this, "Model must be not empty!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(lifetimeS.equals("")){
            JOptionPane.showMessageDialog(this, "LifetimeS must be not empty!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int lifetime = 0;
        try{
            lifetime = Integer.parseInt(lifetimeS);
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Lifetime must be integer number!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(priceS.equals("")){
            JOptionPane.showMessageDialog(this, "Price must be not empty!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int price = 0;
        try{
            price = Integer.parseInt(priceS);
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Price must be integer number!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(lengthS.equals("")){
            JOptionPane.showMessageDialog(this, "Length must be not empty!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int length = 0;
        try{
            length = Integer.parseInt(lengthS);
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Length must be integer number!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(widthS.equals("")){
            JOptionPane.showMessageDialog(this, "Width must be not empty!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int width = 0;
        try{
            width = Integer.parseInt(widthS);
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Width must be integer number!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(heigthS.equals("")){
            JOptionPane.showMessageDialog(this, "Heigth must be not empty!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int heigth = 0;
        try{
            heigth = Integer.parseInt(heigthS);
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Heigth must be integer number!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

  
       
    
    
