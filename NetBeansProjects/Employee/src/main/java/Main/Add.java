    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField1.getText();
        String lastName = jTextField2.getText();
        String patronymic = jTextField3.getText();
        String yearOfBirthS = jTextField4.getText();
        String salaryS = jTextField5.getText();
        int yearOfBirth = 0;
        double salary = 0;
        
        try{
            yearOfBirth = Integer.parseInt(yearOfBirthS);
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Year of birth must be integer number!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try{
            salary = Double.parseDouble(salaryS);
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Salary must be integer number!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Employee emp = new Employee(name, lastName, patronymic, yearOfBirth, salary);
        
        DefaultComboBoxModel<Employee> jList = (DefaultComboBoxModel<Employee>) this.jList.getModel();
        jList.addElement(emp);
        JOptionPane.showMessageDialog(this, "Employee successfully added", "Info", JOptionPane.INFORMATION_MESSAGE);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed


